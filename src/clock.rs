pub fn render_time(time: &str) -> Vec<String> {
    let mut result = vec![String::new(); 10];

    for ch in time.chars() {
        if ch == ':' {
            for i in 0..10 {
                result[i].push_str(COLON[i]);
            }
        } else if let Some(digit) = ch.to_digit(10) {
            let idx = digit as usize;
            for i in 0..10 {
                result[i].push_str(DIGITS[idx][i]);
            }
        }
    }

    result
}
